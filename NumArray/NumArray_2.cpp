class NumArray {
public:
	NumArray(vector<int> &nums) : _nums(nums) { }

	int sumRange(int i, int j) {
		if(j - i < 10)
			return accumulate(_nums.begin() + i, _nums.begin() + j + 1, 0);

		int sum = 0;

		int n = 10;
		int s = 0;
		while(1) {
			if(j / n == i / n){
				break;
			}
			++s;
			n *= 10;
		}
		n /= 10;

		//
		while(_sums.size() < s) {
			_sums.push_back(map<int, int>());
		}

		int big = j / n * n;

		if(i == 0) {
			while(i < big) {
				sum += getSum(i, n);
				i += n;
			}
		} else {
			int m = 10;
			while(i < big) {
				while(i < big && i % m) {
					sum += getSum(i, m/10);
					i += m/10;
				}
				m *= 10;
			}
		}

		n /= 10;
		while(big < j) {

			while(big / n < j / n){
				sum += getSum(big, n);
				big += n;
			}
			n /= 10;
		}
		sum += _nums[j];
		return sum;
	}
//private:
	int getSum(int pos, int distance) {

		if(distance < 10)
			return _nums[pos];

		int i = -1;
		int tmp = distance;
		while(tmp /= 10) {
			++i;
		}

		map<int, int>::iterator iter = _sums[i].find(pos);

		if(iter != _sums[i].end())
			return iter->second;
		else {
			if(i == 0) {
				return _sums[i][pos] = accumulate(_nums.begin() + pos, _nums.begin() + pos + distance, 0);
			} else {
				int sum = 0;
				distance /= 10;
				for(int j = 0; j < 10; ++j){
					sum += getSum(pos + j * distance, distance);
				}
				return _sums[i][pos] = sum;
			}
		}

	}

//private:
	vector<int>& 			_nums;
	vector<map<int, int> >  _sums;
};


